package collection;

import java.io.Serializable;

/**
 * Enum representing the different forms of education available.
 * The available forms are:
 * - DISTANCE_EDUCATION
 * - FULL_TIME_EDUCATION
 * - EVENING_CLASSES
 */
public enum FormOfEducation implements Serializable {
    DISTANCE_EDUCATION,
    FULL_TIME_EDUCATION,
    EVENING_CLASSES;
}
